from fastapi import APIRouter, HTTPException, Depends
from mysite.database.models import Review
from mysite.database.schema import ReviewInputSchema,ReviewOutSchema
from mysite.database.db import SessionLocal
from sqlalchemy.orm import Session
from typing import List

review_router: APIRouter = APIRouter(prefix='/review'  , tags=['Review CRUD'])


async  def get_db():
    db = SessionLocal()
    try:
        yield db
    finally:
        db.close()




@review_router.post('/', response_model=ReviewOutSchema)
async def create_room(room: ReviewInputSchema, db: Session = Depends(get_db)):
    room_db = Review(**room.dict())
    db.add(room_db)
    db.commit()
    db.refresh(room_db)
    return room_db



@review_router.get('/', response_model=List[ReviewOutSchema])
async  def list_review(db: Session = Depends(get_db)):
    return db.query(Review).all()


@review_router.get('/{review_id}/', response_model=ReviewOutSchema)
async  def detail_review(review_id: int, db: Session = Depends(get_db)):
    review_db = db.query(Review).filter(Review.id == review_id).first()
    if not review_db:
        raise HTTPException(detail='Мындай маалымат жок', status_code=400)

    return review_db


@review_router.put('/{review_router}_id}/', response_model=dict)
async def update_review(review_id: int,review: ReviewInputSchema,
                          db: Session = Depends(get_db)):
    review_db = db.query(Review).filter(Review.id==review_id).first()
    if not review_db:
        raise HTTPException(detail='Мындай маалымат жок', status_code=400)

    for review_key, review_value in review.dict().items():
        setattr(review_key, review_value, review_db)

        db.commit()
        db.refresh(review_db)
        return {'massage': 'катгории озгорулду'}


@review_router.delete('/{review}_id}/', response_model=dict)
async def delete_review(review_id, db: Session = Depends(get_db)):
    review_db = db.query(Review).filter(Review.id==review_id).first()
    if not review_db:
        raise HTTPException(detail='Мындай маалымат жок', status_code=400)

    db.delete(review_db)
    db.commit()
    return {'massage': 'саеитегори уда'}